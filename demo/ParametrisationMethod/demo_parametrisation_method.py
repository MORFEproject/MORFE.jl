M = array([[2.0, -1.0],[-1.0, 2.0]])  # stiffness
C = array([[0.01, 0.0],[0.0, 0.01]])  # light damping
K = array([[1.0, 0.0],[0.0, 1.0]])    # mass (highest-order coefficient)

Id = array([[1.0, 0.0],[0.0, 1.0]])    # identity matrix
n = 3
I_red = np.eye(n)       # identity matrix for the reduced space
Zz = np.zeros((2, 2))   # zero matrix
Zz2 = np.zeros((2, 1))  # zero column vector

F = array([[1.0, 1.0]]).T # Force input matrix
external_eig = 1.0j

B = block([
    [Id,    Zz,     Zz2],
    [Zz,    M,      Zz2],
    [Zz2.T, Zz2.T,  1],
])

A1 = block([
    [Zz,    Id,     Zz2],
    [-K,    -C,     F],
    [Zz2.T, Zz2.T,  external_eig]
])

A2 = np.zeros((5, 5**2))
# u1u1, u1u2, u1v1, u1v2, u1r
# u2u1, u2u2, u2v1, u2v2, u2r
# v1u1, v1u2, v1v1, v1v2, v1r
# v2u1, v2u2, v2v1, v2v2, v2r
# r u1, r u2, r v1, r v2, r r
A2[2, 12] = 0.1 # force on v1 with monomial v1v1 
A2[3, 18] = 0.1 # force on v2 with monomial v2v2 

def drag_force(u1, u2):
    return vstack((Zz2, 0.01 * u1 * u2, 0))

A3 = np.zeros((5, 5**3))
A3[2, 0] = 1.0  # force on v1 with monomial u1u1u1
A3[3, 31] = 1.0 # force on v2 with monomial u2u2u2

def cubic_force(u1, u2, u3):
    return vstack((Zz2, 1.0 * u1 * u2 * u3, 0))

A = [A1, A2, A3]



max_expansion_order = 3
f = [None for _ in range(max_expansion_order)] 
W = [None for _ in range(max_expansion_order)]
Gamma = [[None for _ in range(max_expansion_order)] for _ in range(max_expansion_order)]
Xi = [[None for _ in range(max_expansion_order)] for _ in range(max_expansion_order)]

def fill_in_Gamma_Xi(order:int):
    Gamma[order-1][0] = f[order-1]
        
    for k in range(1, order):
        Gamma[order-1][k] = kron(I_red, Gamma[order-2][k-1]) + kron(f[order-1-k], np.eye(n**k))
        
    Xi[order-1][0] = W[order-1]
        
    for k in range(1, order):
        Xi[order-1][k] = np.sum([kron(W[order-sh-2], Xi[sh][k-1]) for sh in range(k-1, order-1)], axis=0)

def compute_residue(order:int):
    if order == 1:
        return A[0] @ W[0] - B @ W[0] @ f[0]
    
    return np.sum([
        B @ W[k] @ Gamma[order-1][k] - A[k] @ Xi[order-1][k] 
        for k in range(order)], axis=0)

